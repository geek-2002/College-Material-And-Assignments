DROP DATABASE IF EXISTS banking;
CREATE DATABASE banking;
USE banking;

CREATE TABLE deposits(
    ACTNO INT NOT NULL,
    CNAME VARCHAR(20),
    BNAME VARCHAR(20),
    AMOUNT NUMERIC(6,2),
    ADATE DATE,
    PRIMARY KEY (ACTNO)
);

CREATE TABLE branches (
    BNAME VARCHAR(20),
    CITY VARCHAR(20)
);

CREATE TABLE customers (
    CNAME VARCHAR(20),
    CITY VARCHAR(20)
);

CREATE TABLE borrows (
    LOANNO INT NOT NULL,
    CNAME VARCHAR(20),
    BNAME VARCHAR(20),
    AMOUNT NUMERIC(6,2),
    PRIMARY KEY (LOANNO)
);

