#include <stdio.h>
int main() {
   
   printf("Size of int data type : %d\n",sizeof(int));
   printf("Size of unsigned int data type : %d\n",sizeof(unsigned int));
   printf("Size of short int data type : %d\n",sizeof(short int));
   printf("Size of unsigned short int data type : %d\n",sizeof(unsigned short int));
   printf("Size of long int data type : %d\n",sizeof(long int));
   printf("Size of unsigned long int data type : %d\n",sizeof(unsigned long int));
   printf("Size of float data type : %d\n",sizeof(float));
   printf("Size of double data type : %d\n",sizeof(double));
   printf("Size of long double data type : %d\n",sizeof(long double));
   printf("Size of char data type : %d\n",sizeof(char));
   printf("Size of unsigned char data type : %d\n",sizeof(unsigned char));
   return 0;
}

