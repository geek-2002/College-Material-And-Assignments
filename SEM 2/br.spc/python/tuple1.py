#---------
print("\n\n-------Access Tuple Items-------")
#by referring to the index number, inside square brackets:

thistuple = ("apple", "banana", "cherry")
print(thistuple[1])


#---------
print("\n\n-------Negative Indexing-------")
#beginning from the end, -1 refers to the last item, -2 refers to the second last item etc

thistuple = ("apple", "banana", "cherry")
print(thistuple[-1])
print(thistuple[-4])


#---------
print("\n\n-------Range of Indexes-------")
#where to start & end
#return value will be a new tuple with the specified items

thistuple = ("apple", "banana", "cherry", "orange", "kiwi", "melon", "mango")
print(thistuple[2:5])











